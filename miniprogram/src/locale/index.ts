/** Reactive locale state */
const state = reactive({
  lang: 'zhHant' as Lang,
  loaded: false,
})

/** Get translated string */
export function t(key: string): string {
  return messages[state.lang]?.[key] || messages.zhHant[key] || key
}

/** Get current language */
export function getLang(): Lang {
  return state.lang
}

/** Set language */
export function setLang(lang: Lang) {
  state.lang = lang
  uni.setStorageSync('lang', lang)
}

/** Load default language from backend, fallback to local storage or zhHant */
export async function initLang() {
  // Try local storage first for instant display
  const cached = uni.getStorageSync('lang') as Lang
  if (cached && messages[cached]) {
    state.lang = cached
  }

  // Then fetch from backend
  try {
    const res = await uni.request({
      url: `${import.meta.env.VITE_API_BASE || '/api/v1/mp'}/app-settings?setting_type=language`,
      method: 'GET',
    })
    if (res.statusCode === 200) {
      const data = res.data as any
      const list = data?.data || data
      const setting = Array.isArray(list) ? list[0] : list
      const defaultLang = setting?.config?.default_lang as Lang
      if (defaultLang && messages[defaultLang]) {
        state.lang = defaultLang
        uni.setStorageSync('lang', defaultLang)
      }
    }
  } catch (e) {
    // Silently fail, use cached or default
  }

  state.loaded = true
}

export default { t, getLang, setLang, initLang, state }
